def get_api_key(name: str) -> str:
    """API 키를 Streamlit secrets 또는 환경변수에서 로드한다.

    Args:
        name: 환경변수/시크릿 이름 (예: ``MOLIT_API_KEY``).

    Returns:
        API 키 문자열. 키가 없으면 빈 문자열을 반환한다.
    """
    try:
        import streamlit as st  # noqa: WPS433
        return st.secrets[name]
    except Exception:
        return os.getenv(name, "")


